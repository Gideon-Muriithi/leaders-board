package com.example.leaderboard.models;

public class Hours {
    private String name;
    private int hours;
    private String country;
    private String badgeUrl;

    public String getName() {
        return name;
    }

    public String getCountry() {
        return country;
    }

    public String getBadgeUrl() {
        return badgeUrl;
    }

    public int getHours() {
        return hours;
    }
}
