package com.example.leaderboard.models;

public class Hours {
    String name;
    int hours;
    String country;
    String badgeUrl;

    public String getName() {
        return name;
    }

    public String getCountry() {
        return country;
    }

    public String getBadgeUrl() {
        return badgeUrl;
    }

    public int getHours() {
        return hours;
    }
}
